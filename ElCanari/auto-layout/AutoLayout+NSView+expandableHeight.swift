import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension NSView {

  //····················································································································

  final func expandableHeight () -> Self {
    self.setContentHuggingPriority (.defaultLow, for: .vertical)
    return self
  }

  //····················································································································

}

