import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension SlavePadStyle {

  //····················································································································

  func descriptionString () -> String {
    switch self {
      case .traversing : return "traversing" // 0
      case .componentSide : return "componentSide" // 1
      case .oppositeSide : return "oppositeSide" // 2
    }
  }

  //····················································································································

}

