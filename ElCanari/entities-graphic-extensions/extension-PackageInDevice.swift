import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   EXTENSION PackageInDevice
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension PackageInDevice {

  //····················································································································

  func acceptToTranslate_PackageInDevice (xBy inDx: Int, yBy inDy: Int) -> Bool {
    return ((self.mX + inDx) >= 0) && ((self.mY + inDy) >= 0)
  }

  //····················································································································

  func translate_PackageInDevice (xBy inDx: Int, yBy inDy: Int, userSet ioSet : ObjcObjectSet) {
    self.mX += inDx
    self.mY += inDy
  }

  //····················································································································

  func operationBeforeRemoving_PackageInDevice () {
    for masterPad in self.mMasterPads {
      for slavePad in masterPad.mSlavePads {
        slavePad.mMasterPad = nil
      }
    }
    super.operationBeforeRemoving ()
  }

  //····················································································································
  //  Alignment Points
  //····················································································································

  func alignmentPoints_PackageInDevice () -> ObjcCanariPointSet {
    return ObjcCanariPointSet ()
  }

  //····················································································································

}

