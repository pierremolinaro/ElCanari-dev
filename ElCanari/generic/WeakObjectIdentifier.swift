import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

struct WeakObjectIdentifier : Equatable {


  //····················································································································
  //   Properties
  //····················································································································

  private weak var mObject : AnyObject?

  //····················································································································
  //   Init
  //····················································································································

  init (_ inObject : AnyObject) {
    self.mObject = inObject
  }

  //····················································································································
  //   Equatable Protocol
  //····················································································································

  static func == (lhs : WeakObjectIdentifier, rhs: WeakObjectIdentifier) -> Bool {
    return lhs.mObject === rhs.mObject
  }

  //····················································································································

}

