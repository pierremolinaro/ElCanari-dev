import Foundation

//--------------------------------------------------------------------------------------------------

struct ProductRect : Codable {

  //································································································
  //  Properties
  //································································································

  let origin : ProductPoint
  let size : ProductSize

  //································································································

  static var zero : ProductRect { ProductRect (origin: .zero, size: .zero) }

  //································································································

  init (origin inOrigin : ProductPoint, size inSize : ProductSize) {
    self.origin = inOrigin
    self.size = inSize
  }

  //································································································

  init (canariRect inCanariRect : CanariRect) {
    self.origin = ProductPoint (canariPoint: inCanariRect.origin)
    self.size = ProductSize (canariSize: inCanariRect.size)
  }

  //································································································

}

