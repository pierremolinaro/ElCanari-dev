import AppKit

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   Status
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

enum Status : UInt, Hashable {
  case ok
  case warning
  case error
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   NetStatusEntry
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

struct NetStatusEntry : Hashable {
  let status : Status
  let isSubnetDescription : Bool
  let string : String
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   StatusStringArray
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

typealias NetStatusEntryArray = [NetStatusEntry]

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   StringArray
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

typealias StringArray = [String]

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   TwoStrings
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

struct TwoStrings : Hashable {
  let mLeft : String
  let mRight : String

  init (_ inLeft : String, _ inRight : String) {
    self.mLeft = inLeft
    self.mRight = inRight
  }
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   TwoStringArray
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

typealias TwoStringArray = [TwoStrings]

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   ThreeStrings
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

struct ThreeStrings : Hashable {
  let mLeft : String
  let mCenter : String
  let mRight : String

  init (_ inLeft : String, _ inCenter : String, _ inRight : String) {
    self.mLeft = inLeft
    self.mCenter = inCenter
    self.mRight = inRight
  }
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   ThreeStringArray
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

typealias ThreeStringArray = [ThreeStrings]

