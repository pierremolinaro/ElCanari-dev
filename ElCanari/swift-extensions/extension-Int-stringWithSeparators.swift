import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension Int {

  //····················································································································

  var stringWithSeparators : String {
    let formatter = NumberFormatter ()
    formatter.numberStyle = .decimal
    formatter.groupingSeparator = Locale.current.groupingSeparator
    return formatter.string (for: self) ?? "\(self)"
  }

  //····················································································································

}

