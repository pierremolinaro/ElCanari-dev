import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class NonPlatedHole : NSView {

  //····················································································································

  override var isFlipped: Bool { return false }

  //····················································································································
  //  Draw
  //····················································································································

  override func draw (_ inDirtyRect : NSRect) {
    var drawingRect = self.bounds.insetBy (dx: 5.0, dy: 5.0)
    NSColor.systemGreen.setFill ()
    NSBezierPath.fill (drawingRect)
    drawingRect = drawingRect.insetBy (dx: 4.0, dy: 4.0)
    NSColor.black.setFill ()
    let bp = NSBezierPath (ovalIn: drawingRect)
    bp.fill ()
  }

  //····················································································································

}

